package com.syntax.class07;

public class Task2 {

	public static void main(String[] args) {
		int x = 1;
		while (x <= 3) {
			System.out.println("hello");
			x++;
		}
		System.out.println("--------------using a do while----------------------");
		int y = 1;
		do {
			System.out.println("Hello");
			y++;
		} while (y <= 3);

	}

	}


